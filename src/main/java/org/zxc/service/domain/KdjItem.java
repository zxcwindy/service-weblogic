package org.zxc.service.domain;

public class KdjItem {

	private String time;
	
	private double k;
	
	private double d;
	
	private double j;
	
	private double m;

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public double getK() {
		return k;
	}

	public void setK(double k) {
		this.k = k;
	}

	public double getD() {
		return d;
	}

	public void setD(double d) {
		this.d = d;
	}

	public double getJ() {
		return j;
	}

	public void setJ(double j) {
		this.j = j;
	}

	public double getM() {
		return m;
	}

	public void setM(double m) {
		this.m = m;
	}
}
